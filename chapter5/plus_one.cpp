#include <iostream>

using namespace std;

int main()
{
    int a =20;
    int b =20;
    cout << "a = " << a << " : b = " << b<< endl;
    cout << "a ++ = " << a++ << ": ++b = " << ++b << endl; //a++=20, a =21 , ++b=21 b=21 注意使用后修改和修改后使用的区别
    cout << "a = " << a << " b =  " << b << endl;

    return 0;
}